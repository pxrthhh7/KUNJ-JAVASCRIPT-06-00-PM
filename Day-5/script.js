function on()
{
    let box = document.getElementById("hp")

    box.style.backgroundColor = "yellow"
}

function off()
{
    let box = document.getElementById("hp")

    box.style.backgroundColor = "black"
}

function round()
{
    let box = document.getElementById("hp")

    box.style.borderRadius = "50%"
}
function squre()
{
    let box = document.getElementById("hp")

    box.style.borderRadius = "0%"
}